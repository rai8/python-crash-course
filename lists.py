friends=["Patrick","Karen","June", "Oscar","Toby"]

print(friends)

#accessing individual items- indexing starts with 0
print(friends[0])

#accessing from back of the list
print(friends[-1])

#accessing within a given range
print(friends[1:3]) #not including 3

#modifying a given member of the list
friends[1]="Mike"

print(friends)